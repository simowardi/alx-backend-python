#!/usr/bin/env python3

"""
Defines a function that concatenates two strings.

Args:
    str1 (str): The first string to be concatenated.
    str2 (str): The second string to be concatenated.

Returns:
    str: The concatenation of the two input strings.
"""

def concat(str1: str, str2: str) -> str:
    return str1 + str2
